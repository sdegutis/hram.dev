static void initfont() {
	UINT8* ptr = sys->font;
	int z = 0;
	for (int sy = 0; sy < SH; sy++) {
		for (int sx = 0; sx < SW; sx++) {
			for (int fy = 0; fy < FH; fy++) {
				for (int fx = 0; fx < FW; fx++) {
					int i = (fy * SW * FW) + (sy * SH * SW * FW) + (sx * FW) + fx;
					*ptr++ = fontdata[i] ? 0xf0 : 0;
				}
			}
		}
	}
}
